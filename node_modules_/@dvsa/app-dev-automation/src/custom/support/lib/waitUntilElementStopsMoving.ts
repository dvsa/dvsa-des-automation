export default async (
  elementSelector: string,
) => {
  const el = await $(elementSelector);

  await el.waitForExist();

  let animating = true;
  let preLoc = el.getLocation();

  while (animating) {
    browser.pause(50);
    const postLoc = el.getLocation();
    // @ts-ignore
    if (preLoc.x === postLoc.x
            // @ts-ignore
            && preLoc.y === postLoc.y) {
      animating = false;
    }

    preLoc = postLoc;
  }
  return true;
};
