import waitUntilElementStopsMoving from '../../lib/waitUntilElementStopsMoving';
import checkIfElementExists from '../../../../boilerplate/support/lib/checkIfElementExists';

/**
 * Perform an click action on the given element
 * @param  {String}   action  The action to perform (click or doubleClick)
 * @param  {String}   selector Element selector
 */
export default async (
  text: string,
) => {
  /**
     * The method to call on the browser object
     * @type {String}
     */
  const elementSelector = `//ion-action-sheet//button[@type='button' and contains(@class,'ion-activatable') and normalize-space(.)='${text}']`;

  await checkIfElementExists(elementSelector);
  await waitUntilElementStopsMoving(elementSelector);

  await $(elementSelector).click();
};
