/**
 * Switch to the NATIVE / WEB Context
 * @param  {String}   mode Context selector
 */
export default async (
  mode: 'native' | 'web',
) => {
  const contexts = await driver.getContexts();

  if (mode === 'native') {
    // Switch to native
    driver.switchContext('NATIVE_APP');
  } else {
    // Switch to web
    driver.switchContext(contexts[1]);
  }
};
