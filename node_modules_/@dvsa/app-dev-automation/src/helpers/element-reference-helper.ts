import * as elementReferences from '../element-references/combined-element-references.json';

export const getElementByReference = (alias: string): string => {
  const elementRefs: any = elementReferences;
  const delimiter = '::';
  if (alias.indexOf(delimiter) > 0) {
    const [page, element] = alias.split(delimiter);
    if (!(elementRefs[page] && elementRefs[page][element])) {
      // throw error if string contains '::' but cannot be found
      throw new Error(`The provided element reference <${alias}> could not be found`);
    } else {
      // return string for element selector if found
      return elementRefs[page][element];
    }
  }
  // return string as is, if it does not contain '::'
  return alias;
};
