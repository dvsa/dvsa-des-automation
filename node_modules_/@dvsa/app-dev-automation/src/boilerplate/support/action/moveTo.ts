import { getElementByReference } from '../../../helpers/element-reference-helper';

/**
 * Move to the given selector with an optional offset on a X and Y position
 * @param  {String}   selector  Element selector
 * @param  {String}   x        X coordinate to move to
 * @param  {String}   y        Y coordinate to move to
 */
export default async (selector: string, x: string, y: string) => {
    const elementSelector: string = getElementByReference(selector);
    /**
     * X coordinate
     * @type {Int}
     */
    const intX = parseInt(x, 10) || undefined;

    /**
     * Y coordinate
     * @type {Int}
     */
    const intY = parseInt(y, 10) || undefined;

    await $(elementSelector).moveTo({
        xOffset: intX,
        yOffset: intY,
    });
};
