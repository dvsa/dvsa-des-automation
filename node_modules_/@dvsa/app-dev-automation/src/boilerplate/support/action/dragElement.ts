import { Selector } from 'webdriverio';
import { getElementByReference } from '../../../helpers/element-reference-helper';

/**
 * Drag a element to a given destination
 * @param  {String}   selector      The selector for the source element
 * @param  {String}   destination The selector for the destination element
 */
export default async (selector: string, destination: Selector) => {
    const elementSelector: string = getElementByReference(selector);
    const target = await $(destination);
    await $(elementSelector).dragAndDrop(target);
};
