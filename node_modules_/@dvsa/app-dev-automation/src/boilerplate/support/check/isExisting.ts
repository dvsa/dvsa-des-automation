import { getElementByReference } from '../../../helpers/element-reference-helper';

/**
 * Check if the given element exists in the current DOM
 * @param  {String}   selector  Element selector
 * @param  {String}   falseCase Whether to check if the element exists or not
 */
export default async (selector: string, falseCase: boolean) => {
    const elementSelector: string = getElementByReference(selector);
    /**
     * Elements found in the DOM
     * @type {Object}
     */
    const elements = await $$(elementSelector);

    if (falseCase) {
        expect(elements).toHaveLength(
            0,
            // @ts-expect-error
            `Expected element "${elementSelector}" not to exist`
        );
    } else {
        expect(elements.length).toBeGreaterThan(
            0,
            // @ts-expect-error
            `Expected element "${elementSelector}" to exist`
        );
    }
};
