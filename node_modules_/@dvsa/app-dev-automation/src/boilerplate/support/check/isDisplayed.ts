import { getElementByReference } from '../../../helpers/element-reference-helper';

/**
 * Check if the given element is (not) visible
 * @param  {String}   selector   Element selector
 * @param  {String}   falseCase Check for a visible or a hidden element
 */
export default async (selector: string, falseCase: boolean) => {
    const elementSelector: string = getElementByReference(selector);
    /**
     * Visible state of the give element
     * @type {String}
     */
    const isDisplayed = await $(elementSelector).isDisplayed();

    if (falseCase) {
        expect(isDisplayed).not.toEqual(
            true,
            // @ts-expect-error
            `Expected element "${elementSelector}" not to be displayed`
        );
    } else {
        expect(isDisplayed).toEqual(
            true,
            // @ts-expect-error
            `Expected element "${elementSelector}" to be displayed`
        );
    }
};
