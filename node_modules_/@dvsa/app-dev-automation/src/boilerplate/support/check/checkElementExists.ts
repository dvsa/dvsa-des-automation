import checkIfElementExists from '../lib/checkIfElementExists';
import { getElementByReference } from '../../../helpers/element-reference-helper';

/**
 * Check if the given element exists
 * @param  {String}   isExisting Whether the element should be existing or not
 *                               (an or no)
 * @param  {String}   selector   Element selector
 */
export default async (isExisting: string, selector: string) => {
    const elementSelector: string = getElementByReference(selector);
    /**
     * Falsecase assertion
     * @type {Boolean}
     */
    let falseCase = true;

    if (isExisting === 'an') {
        falseCase = false;
    }

    await checkIfElementExists(elementSelector, falseCase);
};
