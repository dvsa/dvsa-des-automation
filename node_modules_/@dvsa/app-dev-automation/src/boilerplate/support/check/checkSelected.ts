import { getElementByReference } from '../../../helpers/element-reference-helper';

/**
 * Check the selected state of the given element
 * @param  {String}   selector   Element selector
 * @param  {String}   falseCase Whether to check if the element is elected or
 *                              not
 */
export default async (selector: string, falseCase: boolean) => {
    const elementSelector: string = getElementByReference(selector);
    /**
     * The selected state
     * @type {Boolean}
     */
    const isSelected = await $(elementSelector).isSelected();

    if (falseCase) {
        expect(isSelected)
            // @ts-expect-error
            .not.toEqual(true, `"${elementSelector}" should not be selected`);
    } else {
        expect(isSelected)
            // @ts-expect-error
            .toEqual(true, `"${elementSelector}" should be selected`);
    }
};
