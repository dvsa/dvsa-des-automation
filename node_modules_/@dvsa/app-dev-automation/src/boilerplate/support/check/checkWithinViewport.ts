import { getElementByReference } from '../../../helpers/element-reference-helper';

/**
 * Check if the given element is visible inside the current viewport
 * @param  {String}   selector   Element selector
 * @param  {String}   falseCase Whether to check if the element is visible
 *                              within the current viewport or not
 */
export default async (selector: string, falseCase: boolean) => {
    const elementSelector: string = getElementByReference(selector);
    /**
     * The state of visibility of the given element inside the viewport
     * @type {Boolean}
     */
    const isDisplayed = await $(elementSelector).isDisplayedInViewport();

    if (falseCase) {
        expect(isDisplayed).not.toEqual(
            true,
            // @ts-expect-error
            `Expected element "${elementSelector}" to be outside the viewport`
        );
    } else {
        expect(isDisplayed).toEqual(
            true,
            // @ts-expect-error
            `Expected element "${elementSelector}" to be inside the viewport`
        );
    }
};
