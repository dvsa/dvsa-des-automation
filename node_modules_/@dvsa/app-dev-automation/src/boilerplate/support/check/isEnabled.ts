import { getElementByReference } from '../../../helpers/element-reference-helper';

/**
 * Check if the given selector is enabled
 * @param  {String}   selector   Element selector
 * @param  {String}   falseCase Whether to check if the given selector
 *                              is enabled or not
 */
export default async (selector: string, falseCase: boolean) => {
    const elementSelector: string = getElementByReference(selector);
    /**
     * The enabled state of the given selector
     * @type {Boolean}
     */
    const isEnabled = await $(elementSelector).isEnabled();

    if (falseCase) {
        expect(isEnabled).not.toEqual(
            true,
            // @ts-expect-error
            `Expected element "${elementSelector}" not to be enabled`
        );
    } else {
        expect(isEnabled).toEqual(
            true,
            // @ts-expect-error
            `Expected element "${elementSelector}" to be enabled`
        );
    }
};
