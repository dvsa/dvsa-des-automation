import { getElementByReference } from '../../src/helpers/element-reference-helper';

describe('this test', () => {
  it('should return the correct element reference when passes string with "::" format', () => {
    const result = getElementByReference('common-log-in::ms-continue');
    expect(result).toEqual('//XCUIElementTypeButton[@name=\'Continue\']');
  });
  it('should return the original string if incorrect format', () => {
    const result = getElementByReference('#page-title');
    expect(result).toEqual('#page-title');
  });
  it('should return the original string if value not found', () => {
    try {
      getElementByReference('missing-page::missing-element');
    } catch (error) {
      expect((error as Error).message).toEqual('The provided element reference <missing-page::missing-element> could not be found');
    }
  });
});
